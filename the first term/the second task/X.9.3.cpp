#include <iostream>
#include <cmath>
using namespace std;

double dxdt(double y){
    return y;
}

double dydt(double x, double y, double mu){
    return mu * (1 - pow(y, 2)) * y - x;
}

int main() {
    const double mu = 1000.0, T = 1000.0, x_0 = 0.0, y_0 = 0.001;
    double x[10000], y[10000], tau;
    double k1[2], k2[2], k3[2], k4[2], k5[2], k6[2], k7[2], x0, y0, epsilon;
    int N;
    tau = 0.001;
    N = int(T / tau);
    x[0] = x_0;
    y[0] = y_0;
    cin >> epsilon;
    for (int i = 1; i <= N; i++) {
        x0 = x[i-1];
        y0 = y[i-1];

        k1[0] = dxdt(y0);
        k1[1] = dydt(x0, y0, mu);

        k2[0] = dxdt(y0 + tau * (0.2 * k1[1]));
        k2[1] = dydt(x0 + tau * (0.2 * k1[0]), y0 + tau * (0.2 * k1[1]), mu);

        k3[0] = dxdt(y0 + tau * ((3.0 / 40.0) * k1[1] + (9.0 / 40.0) * k2[1]));
        k3[1] = dydt(x0 + tau * ((3.0 / 40.0) * k1[0] + (9.0 / 40.0) * k2[0]), y0 + tau * ((3.0 / 40.0) * k1[1] + (9.0 / 40.0) * k2[1]), mu);

        k4[0] = dxdt(y0 + tau * ((44.0 / 45.0) * k1[1] + (-56.0 / 15.0) * k2[1] + (32.0 / 9.0) * k3[1]));
        k4[1] = dydt(x0 + tau * ((44.0 / 45.0) * k1[0] + (-56.0 / 15.0) * k2[0] + (32.0 / 9.0) * k3[0]), y0 + tau * ((44.0 / 45.0) * k1[1] + (-56.0 / 15.0) * k2[1] + (32.0 / 9.0) * k3[1]), mu);

        k5[0] = dxdt(y0 + tau * ((19372.0 / 6561.0) * k1[1] + (-25360.0 / 2187.0) * k2[1] + (64448.0 / 6561.0) * k3[1] + (-212.0 / 729.0) * k4[1]));
        k5[1] = dydt(x0 + tau * ((19372.0 / 6561.0) * k1[0] + (-25360.0 / 2187.0) * k2[0] + (64448.0 / 6561.0) * k3[0] + (-212.0 / 729.0) * k4[0]), y0 + tau * ((19372.0 / 6561.0) * k1[1] + (-25360.0 / 2187.0) * k2[1] + (64448.0 / 6561.0) * k3[1] + (-212.0 / 729.0) * k4[1]), mu);

        k6[0] = dxdt(y0 + tau * ((9017.0 / 3168.0) * k1[1] + (-355.0 / 33.0) * k2[1] + (46732.0 / 5247.0) * k3[1] + (49.0 / 176.0) * k4[1] + (-5103.0 / 18656.0) * k5[1]));
        k6[1] = dydt(x0 + tau * ((9017.0 / 3168.0) * k1[0] + (-355.0 / 33.0) * k2[0] + (46732.0 / 5247.0) * k3[0] + (49.0 / 176.0) * k4[0] + (-5103.0 / 18656.0) * k5[0]), y0 + tau * ((9017.0 / 3168.0) * k1[1] + (-355.0 / 33.0) * k2[1] + (46732.0 / 5247.0) * k3[1] + (49.0 / 176.0) * k4[1] + (-5103.0 / 18656.0) * k5[1]), mu);

        k7[0] = dxdt(y0 + tau * ((35.0 / 384.0) * k1[1] + (500.0 / 1113.0) * k3[1] + (125.0 / 192.0) * k4[1] + (-2187.0 / 6784.0) * k5[1] + (11.0 / 84.0) * k6[1]));
        k7[1] = dydt(x0 + tau * ((35.0 / 384.0) * k1[0] + (500.0 / 1113.0) * k3[0] + (125.0 / 192.0) * k4[0] + (-2187.0 / 6784.0) * k5[0] + (11.0 / 84.0) * k6[0]), y0 + tau * ((35.0 / 384.0) * k1[1] + (500.0 / 1113.0) * k3[1] + (125.0 / 192.0) * k4[1] + (-2187.0 / 6784.0) * k5[1] + (11.0 / 84.0) * k6[1]), mu);

        x[i] = x0 + tau * ((35.0 / 384.0) * k1[0] + (500.0 / 1113.0) * k3[0] + (125.0 / 192.0) * k4[0] + (-2187.0 / 6784.0) * k5[0] + (11.0 / 84.0) * k6[0]);
        y[i] = y0 + tau * ((35.0 / 384.0) * k1[1] + (500.0 / 1113.0) * k3[1] + (125.0 / 192.0) * k4[1] + (-2187.0 / 6784.0) * k5[1] + (11.0 / 84.0) * k6[1]);
        tau *= pow(epsilon * tau / (2 * abs((71.0 / 57600.0) * k1[0] - 71.0 * k3[0] / 16695.0 + 71.0 * k4[0] / 1920.0 - 17253.0 * k5[0] / 339200.0 + 22.0 * k6[0] / 525.0 - 1.0 * k7[0] / 40.0)), 0.2);

    }
    cout << tau << endl;
    cout << x[N] << endl;
    cout << y[N] << endl;
}
